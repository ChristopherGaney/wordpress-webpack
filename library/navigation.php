// Register HTML5 Blank Navigation
function register_rlc_menu() {
    register_nav_menus(array( // Using array to specify more menus if needed
        'mainNav' => __('Main Nav', 'redLetterTheme'),
        'footerNav' => __('Footer Nav', 'redLetterTheme'),
        'off-canvas' => __('Off Canvas', 'redLetterTheme')
        /*'socialNav' => __('Social Nav', 'redLetterTheme') */
        
    ));
}
// Remove the <div> surrounding the dynamic navigation to cleanup markup
function my_wp_nav_menu_args($args = '')
{
    $args['container'] = false;
    return $args;
}

// Remove Injected classes, ID's and Page ID's from Navigation <li> items
function my_css_attributes_filter($var)
{
    return is_array($var) ? array() : '';
}

function main_nav() {
    wp_nav_menu(
    array(
        'theme_location'  => 'mainNav',
        'menu'            => '',
        'container'       => 'div',
        'container_class' => 'menu-{menu slug}-container',
        'container_id'    => '',
        'menu_class'      => 'menu',
        'menu_id'         => '',
        'echo'            => true,
        'fallback_cb'     => 'wp_page_menu',
        'before'          => '',
        'after'           => '',
        'link_before'     => '',
        'link_after'      => '',
        'items_wrap'      => '<ul>%3$s</ul>',
        'item_spacing'    => 'discard', // remove whitespace added between li's
        'depth'           => 0,
        'walker'          => ''
        )
    );
}
